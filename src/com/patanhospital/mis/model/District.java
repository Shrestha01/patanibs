package com.patanhospital.mis.model;

public class District {
	private short fldDistrictCode;
	private String fldDistrictName;
	
	public short getFldDistrictCode() {
		return fldDistrictCode;
	}
	public void setFldDistrictCode(short fldDistrictCode) {
		this.fldDistrictCode = fldDistrictCode;
	}
	public String getFldDistrictName() {
		return fldDistrictName;
	}
	public void setFldDistrictName(String fldDistrictName) {
		this.fldDistrictName = fldDistrictName;
	}
	
	
}
