package com.patanhospital.mis.model;

public class WardType {
	
	private String fldWardID;
	private String fldWardDesc;
	private short fldBedCount;
	
	public String getFldWardID() {
		return fldWardID;
	}
	public void setFldWardID(String fldWardID) {
		this.fldWardID = fldWardID;
	}
	public String getFldWardDesc() {
		return fldWardDesc;
	}
	public void setFldWardDesc(String fldWardDesc) {
		this.fldWardDesc = fldWardDesc;
	}
	public short getFldBedCount() {
		return fldBedCount;
	}
	public void setFldBedCount(short fldBedCount) {
		this.fldBedCount = fldBedCount;
	}
	
	
}
